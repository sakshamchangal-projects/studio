'use client';

import type { AnalyzeAudioSampleOutput } from '@/ai/flows/analyze-audio-sample';
import {
  Accordion,
  AccordionContent,
  AccordionItem,
  AccordionTrigger,
} from '@/components/ui/accordion';
import {
  Card,
  CardContent,
  CardHeader,
  CardTitle,
} from '@/components/ui/card';
import { Progress } from '@/components/ui/progress';
import { BrainCircuit, Languages, Percent, User } from 'lucide-react';

interface AnalysisResultProps {
  result: AnalyzeAudioSampleOutput;
}

const ClassificationIcon = ({
  classification,
}: {
  classification: 'AI_GENERATED' | 'HUMAN';
}) => {
  if (classification === 'AI_GENERATED') {
    return <BrainCircuit className="h-16 w-16 text-destructive" />;
  }
  return <User className="h-16 w-16 text-primary" />;
};

export function AnalysisResult({ result }: AnalysisResultProps) {
  const confidencePercentage = Math.round(result.confidenceScore * 100);

  return (
    <div className="space-y-6">
      <Card className="text-center">
        <CardContent className="p-6">
          <ClassificationIcon classification={result.classification} />
          <h2 className="mt-4 text-2xl font-bold">
            {result.classification === 'AI_GENERATED'
              ? 'AI Generated'
              : 'Human Voice'}
          </h2>
          <p className="text-muted-foreground">
            {result.classification === 'AI_GENERATED'
              ? 'This audio sample is likely generated by an AI.'
              : 'This audio sample appears to be from a human speaker.'}
          </p>
        </CardContent>
      </Card>

      <div className="grid grid-cols-1 gap-4 sm:grid-cols-2">
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">
              Confidence Score
            </CardTitle>
            <Percent className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{confidencePercentage}%</div>
            <Progress
              value={confidencePercentage}
              className="mt-2 h-2"
              aria-label={`Confidence score of ${confidencePercentage}%`}
            />
          </CardContent>
        </Card>
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">
              Detected Language
            </CardTitle>
            <Languages className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">
              {result.detectedLanguage || 'N/A'}
            </div>
            <p className="text-xs text-muted-foreground">
              Language of the audio sample
            </p>
          </CardContent>
        </Card>
      </div>

      <Accordion type="single" collapsible>
        <AccordionItem value="item-1">
          <AccordionTrigger>View Raw JSON Output</AccordionTrigger>
          <AccordionContent>
            <pre className="mt-2 w-full overflow-x-auto rounded-md bg-secondary p-4 text-sm">
              <code>{JSON.stringify(result, null, 2)}</code>
            </pre>
          </AccordionContent>
        </AccordionItem>
      </Accordion>
    </div>
  );
}
